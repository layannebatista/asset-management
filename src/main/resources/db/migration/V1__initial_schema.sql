foreign key (from_unit_id)
references units(id);

alter table transfer_requests
add constraint fk_transfer_to_unit
foreign key (to_unit_id)
references units(id);

alter table transfer_requests
add constraint fk_transfer_requested_by
foreign key (requested_by)
references users(id);

alter table transfer_requests
add constraint fk_transfer_approved_by
foreign key (approved_by)
references users(id);

